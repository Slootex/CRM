  function closeTracking() {
    document.getElementById('custom-tracking-modal').classList.add("hidden");
    document.getElementById("statusDiv").remove();

  }

  function showCustomTrackingModal(process_id, label) {
    loadData();
$.get("{{url("/")}}/crm/shippinghistory/status/" + process_id + "/" + label, function(data) {
  document.getElementById("custom-tracking-modal-statuses").innerHTML = "";


    if(data != null) {

        let ausgang = data[0];
        let trackings = data[1];
        let db_trackings = data[2];


        document.getElementById("custom-tracking-modal-companyname").innerHTML  = ausgang["companyname"];
        document.getElementById("custom-tracking-modal-person").innerHTML       = ausgang["firstname"] + " " + ausgang["lastname"];
        document.getElementById("custom-tracking-modal-street").innerHTML       = ausgang["street"]     + " "   + ausgang["streetno"];
        document.getElementById("custom-tracking-modal-locality").innerHTML     = ausgang["zipcode"]    + ", "  + ausgang["city"];
        document.getElementById("custom-tracking-modal-country").innerHTML      = ausgang["country"];
        document.getElementById("custom-tracking-modal-email").innerHTML        = ausgang["email"];
        
        if(ausgang["phonenumber"] != null) { 
          document.getElementById("custom-tracking-modal-phonenumbers").innerHTML = ausgang["phonenumber"];
        }
        if(ausgang["mobilnumber"] != null) {
          document.getElementById("custom-tracking-modal-phonenumbers").innerHTML += " " + ausgang["mobilnumber"];
        }

        document.getElementById("custom-tracking-modal-extern-ups").href        = "http://wwwapps.ups.com/WebTracking/track?track=yes&trackNums=" + ausgang["label"] + "&loc=de_de";
        document.getElementById("custom-tracking-modal-extern-ups").innerHTML   = ausgang["label"];

        document.getElementById("custom-tracking-modal-labelfile").href = "{{url('/')}}/temp/" + ausgang["process_id"] + ausgang["shortcut"] + ".png";

       
        if(trackings != null) {
            let trackingModalCounter = 1;
            let trackingModalLength  = trackings.length;

            trackings.forEach(tracking => {
                let rawDate = tracking["date"];
                let year    = rawDate.substr(0, 4);
                let month   = rawDate.substr(4, 2);
                let day     = rawDate.substr(6, 2);

                let statusDiv = document.createElement("div");
                statusDiv.setAttribute("id", "statusDiv");

                let status  = document.createElement("p");
                status.classList.add("text-center", "text-yellow-600", "py-2");
                if(tracking["status"]["description"] == "Stornierte Info erhalten ") {
                  if(ausgang["back"] == "yes") {
                    status.innerHTML = '<span>' + day + "." + month + "." + year + '</span> <span class="text-black">|</span> <span class="text-black">Warenausgang zurück</span>';
                  } else {
                    status.innerHTML = '<span>' + day + "." + month + "." + year + '</span> <span class="text-black">|</span> <span class="text-black">' + tracking["status"]["description"] + '</span>'
                  }
                } else {
                  status.innerHTML = '<span>' + day + "." + month + "." + year + '</span> <span class="text-black">|</span> <span class="text-black">' + tracking["status"]["description"] + '</span>'
                }         
                statusDiv.appendChild(status);


                if(trackingModalCounter != trackingModalLength) {
                            let arrowDown = document.createElement("div");
                            arrowDown.innerHTML = '<div class="m-auto" style="margin-left: -4rem;">     <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="w-8 h-8 m-auto"><path stroke-linecap="round" stroke-linejoin="round" d="M12 19.5v-15m0 0l-6.75 6.75M12 4.5l6.75 6.75" /></svg></div>';
                }

                trackingModalCounter++;

                document.getElementById("custom-tracking-modal-statuses").appendChild(statusDiv);
            });

        } else {
          if(db_trackings != null) {
                let trackingModalCounter = 1;
                let trackingModalLength  = db_trackings.length;
                db_trackings.forEach(tracking => {

                    let statusDiv = document.createElement("div");

                    let status  = document.createElement("p");
                    status.classList.add("text-center", "text-yellow-600", "py-2");
                    status.innerHTML = tracking["created_at"] + '<span class="text-black">|</span> <span class="text-black">' + tracking["status"] + '</span>';
                    
                    statusDiv.appendChild(status);


                    if(trackingModalCounter != trackingModalLength) {
                        let arrowDown = document.createElement("div");
                        arrowDown.innerHTML = '<div class="m-auto" style="margin-left: -4rem;">     <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="w-8 h-8 m-auto"><path stroke-linecap="round" stroke-linejoin="round" d="M12 19.5v-15m0 0l-6.75 6.75M12 4.5l6.75 6.75" /></svg></div>';
                    }

                    trackingModalCounter++;

                    document.getElementById("custom-tracking-modal-db-statuses").appendChild(statusDiv);
                });
            }
        }

        document.getElementById("custom-tracking-modal-packet-info").innerHTML          = ": " + ausgang["length"] + "x" + ausgang["width"] + "x" + ausgang["height"] + " / " + ausgang["weight"] + " kg";
        if(ausgang["info"] != null) {
          document.getElementById("custom-tracking-modal-info").innerHTML               = ": " + ausgang["info"];
        }
        if(ausgang["carriers_service"] == "65") {
          document.getElementById("custom-tracking-modal-info-shippingtype").innerHTML    = ": UPS Standard";
        }

        document.getElementById("custom-tracking-modal-zurück").setAttribute("onclick", 'warenausgangZurück("' + ausgang["process_id"] + '", "' + ausgang["label"] + '", "' + ausgang["created_at"] + '")');
        document.getElementById("custom-tracking-modal-löschen").setAttribute("onclick", 'labelLöschen("' + ausgang["process_id"] + '", "' + ausgang["label"] + '")');

    }
    savedPOST();
    document.getElementById("custom-tracking-modal").classList.remove("hidden");
});

}

  function warenausgangZurück(process_id, label, date) {
    loadData();
    $.get('{{url("/")}}/crm/warenausgang/zurück/' + label, function ( data ) {
      if(document.getElementById(label + "-trackinghistory-status").innerHTML == "storniert") {
        document.getElementById(label + "-trackinghistory-status").innerHTML = "storniert + wa, rückgängig";
      }
      savedPOST();
      document.getElementById("warenausgangZurückModal").classList.remove('hidden');
    }); 
    
  }

  function labelLöschen(process_id, label) {
    loadData();
    $.get('{{url("/")}}/crm/warenausgang/label/löschen/' + label, function ( data ) {
      
      savedPOST();
      document.getElementById('deleteLabelModal').classList.remove('hidden');
    }); 
  }

